# Ввод значений сопротивлений
R1 = float(input("Введите сопротивление R1 (Ом): "))
R2 = float(input("Введите сопротивление R2 (Ом): "))
R3 = float(input("Введите сопротивление R3 (Ом): "))

# Вычисление общего сопротивления
R_total = 1 / (1/R1 + 1/R2 + 1/R3)

# Вывод результата
print(f"Общее сопротивление параллельного соединения: {R_total:.2f} Ом")