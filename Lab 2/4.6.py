# Ввод сторон треугольника
a = float(input("Введите длину первой стороны (см): "))
b = float(input("Введите длину второй стороны (см): "))
c = float(input("Введите длину третьей стороны (см): "))

# Проверка на существование треугольника
if a + b > c and a + c > b and b + c > a:
    # Вычисление периметра
    perimeter = a + b + c

    # Вычисление площади по формуле Герона
    p = perimeter / 2  # полупериметр
    area = (p * (p - a) * (p - b) * (p - c)) ** 0.5

    # Вывод результатов
    print(f"\nРезультаты вычислений:")
    print(f"Периметр треугольника: {perimeter:.2f} см")
    print(f"Площадь треугольника: {area:.2f} см²")
else:
    print("Ошибка: Треугольник с такими сторонами не существует!")