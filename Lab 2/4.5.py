# Ввод катетов прямоугольного треугольника
a = float(input("Введите длину первого катета (см): "))
b = float(input("Введите длину второго катета (см): "))

# Вычисление гипотенузы
c = (a**2 + b**2)**0.5

# Вычисление периметра и площади
perimeter = a + b + c
area = (a * b) / 2

# Вывод результатов
print(f"\nРезультаты вычислений:")
print(f"Гипотенуза: {c:.2f} см")
print(f"Периметр: {perimeter:.2f} см")
print(f"Площадь: {area:.2f} см²")