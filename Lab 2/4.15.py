# Ввод начальной скорости
V0 = float(input("Введите начальную скорость V0 (м/с): "))

# Ввод ускорения
a = float(input("Введите ускорение a (м/с²): "))

# Ввод времени
t = float(input("Введите время движения t (секунд): "))

# Вычисление пройденного расстояния
S = V0 * t + (a * t ** 2) / 2

# Вывод результата
print(f"Пройденное расстояние: {S:.2f} метров")
